export default Vue.extend({
  props: {
    level: {
      type: Number
    },
    continue_url: {
      type: String
    }
  },
  methods: {
    itemClick(level) {
      if (level > this.level) {
        return this.$dialog
          .warning()
          .message('شما اجازه ندارید مرحله های بعدی را زودتر انجام دهید')
